// Primitive / Value Types
// String, number, boolean, undefined, null, symbol
// JS -> Dynamic language = You can change the data type of a variable on the go.

// String
let x = "Pratik";
console.log(x);
console.log(typeof(x));

// Number
let y = 123;
console.log(y);
console.log(typeof(y));

let z = 1.5;
console.log(z);
console.log(typeof(z));

// Boolean
let p = true;
console.log(p);
console.log(typeof(p));

// Undefined
let q;
console.log(q);
console.log(typeof(q));
q = "Pratik";
console.log(typeof(q));

// Null
let r = null;
console.log(r);
console.log(typeof(r));